// 归一化：小写 + 去除空白（不影响中文，便于 latin 词匹配）
function normalize(s) {
  return String(s || "").toLowerCase().replace(/\s+/g, "");
}

/**
 * 判定用户文本是否越界（非医疗请求）。
 * @param {string} text 用户问题原文
 * @returns {{ outOfScope: boolean, reason?: string }}
 */
export function detectScope(text) {
  const s = normalize(text);
  if (!s) return { outOfScope: false }; // 空文本不拦截

  for (const w of NON_MEDICAL_HINTS) {
    if (s.includes(w)) {
      return { outOfScope: true, reason: `命中非医疗关键词「${w}」` };
    }
  }
  for (const w of MEDICAL_HINTS) {
    if (s.includes(w)) {
      return { outOfScope: false, reason: `命中医疗意图「${w}」` };
    }
  }
  // 既非明确非医疗、也非明确医疗 → 保守放行（不误伤边界问题）
  return { outOfScope: false };
}

/** 便捷布尔判定。 */
export function isOutOfScope(text) {
  return detectScope(text).outOfScope;
}

